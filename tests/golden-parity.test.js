/**
 * Golden Snapshot Parity Tests
 *
 * Asserts that lib/terravalue-engine.js produces output identical to the
 * committed snapshots in tests/golden-snapshots.json.
 *
 * If a test fails, EITHER:
 *   (a) The engine math regressed unintentionally — fix the engine.
 *   (b) The engine math changed intentionally — re-run
 *       `node tests/generate-snapshots.js` and commit the updated snapshot
 *       file alongside the engine change. The diff in PR review shows
 *       reviewers exactly which outputs moved and by how much.
 *
 * Run: node --test tests/golden-parity.test.js
 *      OR: npm test
 */

