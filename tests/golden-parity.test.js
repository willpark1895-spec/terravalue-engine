// Freeze time before requiring the engine. Must match generate-snapshots.js
// exactly. Any drift between these two values means snapshot regeneration
// produces output that the test then can't match.
const FROZEN_ISO = '2026-05-20T00:00:00.000Z';
const FROZEN_NOW = new Date(FROZEN_ISO).getTime();
const RealDate = Date;
Date.now = () => FROZEN_NOW;
global.Date = new Proxy(RealDate, {
  construct(target, args) {
    if (args.length === 0) return new target(FROZEN_NOW);
    return new target(...args);
  },
});
global.Date.now = () => FROZEN_NOW;

// Match generate-snapshots.js: deterministic Math.random.
function mulberry32(seed) {
  let s = seed >>> 0;
  return function() {
    s = (s + 0x6D2B79F5) >>> 0;
    let t = s;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
let _rng = mulberry32(1);
Math.random = () => _rng();
function resetRng(seed = 1) { _rng = mulberry32(seed); }

